package com.example.firebasedemo.model.user;

public class Category {
    private String slug;
    private String pretty_slug;

    public String getSlug() {
        return slug;
    }

    public void setSlug(String slug) {
        this.slug = slug;
    }

    public String getPretty_slug() {
        return pretty_slug;
    }

    public void setPretty_slug(String pretty_slug) {
        this.pretty_slug = pretty_slug;
    }
}
